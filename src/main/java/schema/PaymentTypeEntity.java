@Entity
@Table(name = "PaymentType", schema = "", catalog = "Podatkova")
public class PaymentTypeEntity {
    private int paymentType;
    private String name;

    @Id
    @Column(name = "PaymentType_", nullable = false, insertable = true, updatable = true)
    public int getPaymentType() {
        return paymentType;
    }

    public void setPaymentType(int paymentType) {
        this.paymentType = paymentType;
    }

    @Basic
    @Column(name = "Name", nullable = false, insertable = true, updatable = true, length = 50)
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        PaymentTypeEntity that = (PaymentTypeEntity) o;

        if (paymentType != that.paymentType) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = paymentType;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}
